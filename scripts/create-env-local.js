#!/usr/bin/env node
/**
 * Creates .env.local for Appwrite + Scriptony functions.
 *
 * Usage:
 *   node scripts/create-env-local.js <VITE_APPWRITE_ENDPOINT> <VITE_APPWRITE_PROJECT_ID> [VITE_BACKEND_API_BASE_URL]
 *   npm run env:local -- http://localhost:8080/v1 YOUR_PROJECT_ID https://your-functions-host/.../v1
 */
const fs = require("fs");
const path = require("path");

const endpoint = process.env.VITE_APPWRITE_ENDPOINT || process.argv[2];
const projectId = process.env.VITE_APPWRITE_PROJECT_ID || process.argv[3];
const fnBase =
  process.env.VITE_BACKEND_API_BASE_URL ||
  process.env.VITE_APPWRITE_FUNCTIONS_BASE_URL ||
  process.argv[4] ||
  "";

if (!endpoint?.trim() || !projectId?.trim()) {
  console.error("Appwrite endpoint und Projekt-ID fehlen.");
  console.error(
    "  node scripts/create-env-local.js <VITE_APPWRITE_ENDPOINT> <VITE_APPWRITE_PROJECT_ID> [functions base URL]",
  );
  process.exit(1);
}

const lines = [
  `# Generated by scripts/create-env-local.js`,
  `VITE_APPWRITE_ENDPOINT=${endpoint.trim()}`,
  `VITE_APPWRITE_PROJECT_ID=${projectId.trim()}`,
];

if (fnBase.trim()) {
  lines.push(`VITE_BACKEND_API_BASE_URL=${fnBase.trim()}`);
}

lines.push(
  `VITE_APP_WEB_URL=http://localhost:3000`,
  `VITE_AUTH_REDIRECT_URL=http://localhost:3000`,
  `VITE_PASSWORD_RESET_REDIRECT_URL=http://localhost:3000/reset-password`,
  ``,
);

const outPath = path.join(__dirname, "..", ".env.local");
fs.writeFileSync(outPath, lines.join("\n"), "utf8");
console.log("Written", outPath);
